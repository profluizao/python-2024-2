# Faça um programa usando Linguagem Python, que leia o nome o endereço e o telefone de um cliente e ao final,
# imprima esses dados.

# Solicita ao usuário o nome, endereço e telefone
nome = input("Digite o nome do cliente: ")
endereco = input("Digite o endereço do cliente: ")
telefone = input("Digite o telefone do cliente: ")

# Exibe os dados informados
print("\nInformações do Cliente:")
print(f"Nome: {nome}")
print(f"Endereço: {endereco}")
print(f"Telefone: {telefone}")
