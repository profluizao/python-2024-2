# Faça um programa usando Linguagem Python, que peça ao usuário que digite dois números e mostra o produto deles.

# Solicita ao usuário para digitar dois números
numero1 = float(input("Digite o primeiro número: "))
numero2 = float(input("Digite o segundo número: "))

# Calcula o produto dos dois números
produto = numero1 * numero2

# Imprime o resultado do produto
print("O produto dos dois números é:", produto)
